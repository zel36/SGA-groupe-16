CREATE OR REPLACE TRIGGER trg_badge_unique_actif
BEFORE INSERT OR UPDATE ON BADGE
FOR EACH ROW
DECLARE v_count NUMBER;
BEGIN
    IF :NEW.statut = 'ACTIF' THEN
        SELECT COUNT(*) INTO v_count FROM BADGE
        WHERE id_personne = :NEW.id_personne AND statut = 'ACTIF' AND num_badge != :NEW.num_badge;
        IF v_count > 0 THEN
            RAISE_APPLICATION_ERROR(-20001,'Cette personne possède déjà un badge actif.');
        END IF;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER trg_audit_non_modifiable
BEFORE UPDATE OR DELETE ON HISTORIQUE_EVENEMENT
FOR EACH ROW
BEGIN
    RAISE_APPLICATION_ERROR(-20003,'Le journal d''audit est NON MODIFIABLE.');
END;
/

CREATE OR REPLACE TRIGGER trg_acces_lieu_bloque
BEFORE INSERT ON HISTORIQUE_EVENEMENT
FOR EACH ROW
DECLARE v_statut VARCHAR2(20);
BEGIN
    IF :NEW.type_evenement = 'ACCES_PHYSIQUE' AND :NEW.id_lieu IS NOT NULL THEN
        SELECT statut INTO v_statut FROM LIEU WHERE id_lieu = :NEW.id_lieu;
        IF v_statut = 'BLOQUE' AND :NEW.statut_resultat = 'ACCORDE' THEN
            RAISE_APPLICATION_ERROR(-20004,'Accès impossible : lieu BLOQUÉ.');
        END IF;
    END IF;
EXCEPTION WHEN NO_DATA_FOUND THEN
    RAISE_APPLICATION_ERROR(-20005,'Lieu introuvable.');
END;
/

CREATE OR REPLACE TRIGGER trg_trace_changement_profil
AFTER UPDATE OF id_profil ON PERSONNE
FOR EACH ROW
BEGIN
    IF NVL(:OLD.id_profil,0) != NVL(:NEW.id_profil,0) THEN
        INSERT INTO HISTORIQUE_PROFIL (id_personne,id_ancien_profil,id_nouveau_profil,motif,effectue_par)
        VALUES (:NEW.id_personne,:OLD.id_profil,:NEW.id_profil,'Changement de profil',USER);
    END IF;
END;
/

