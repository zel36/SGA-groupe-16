-- ============================================================================
-- 1. PROFIL
-- ============================================================================
CREATE TABLE PROFIL (
    id_profil       NUMBER GENERATED BY DEFAULT AS IDENTITY,
    libelle_profil  VARCHAR2(50)  NOT NULL,
    description     VARCHAR2(255),
    date_creation   TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,
    statut          VARCHAR2(20)  DEFAULT 'ACTIF' NOT NULL,
    CONSTRAINT pk_profil         PRIMARY KEY (id_profil),
    CONSTRAINT uq_libelle_profil UNIQUE (libelle_profil),
    CONSTRAINT chk_profil_statut CHECK (statut IN ('ACTIF','INACTIF'))
);

-- ============================================================================
-- 2. PERSONNE
-- ============================================================================
CREATE TABLE PERSONNE (
    id_personne    NUMBER GENERATED BY DEFAULT AS IDENTITY,
    nom            VARCHAR2(100) NOT NULL,
    prenom         VARCHAR2(100) NOT NULL,
    date_naissance DATE          NOT NULL,
    fonction       VARCHAR2(50)  NOT NULL,
    login          VARCHAR2(50)  NOT NULL,
    mot_de_passe   VARCHAR2(256) NOT NULL,
    statut         VARCHAR2(20)  DEFAULT 'ACTIF' NOT NULL,
    id_profil      NUMBER,
    CONSTRAINT pk_personne           PRIMARY KEY (id_personne),
    CONSTRAINT uq_personne_login     UNIQUE (login),
    CONSTRAINT fk_personne_profil    FOREIGN KEY (id_profil) REFERENCES PROFIL(id_profil),
    CONSTRAINT chk_personne_statut   CHECK (statut IN ('ACTIF','INACTIF')),
    CONSTRAINT chk_personne_fonction CHECK (fonction IN ('ETUDIANT','ENSEIGNANT','PERSONNEL','AGENT_SECURITE','ADMINISTRATEUR','VISITEUR'))
);

-- ============================================================================
-- 3. LIEU
-- ============================================================================
CREATE TABLE LIEU (
    id_lieu         NUMBER GENERATED BY DEFAULT AS IDENTITY,
    libelle_lieu    VARCHAR2(100) NOT NULL,
    emplacement     VARCHAR2(150) NOT NULL,
    capacite_max    NUMBER(4)     DEFAULT 1 NOT NULL,
    heure_ouverture NUMBER(4)     DEFAULT 700  NOT NULL,
    heure_fermeture NUMBER(4)     DEFAULT 2200 NOT NULL,
    statut          VARCHAR2(20)  DEFAULT 'OUVERT' NOT NULL,
    CONSTRAINT pk_lieu           PRIMARY KEY (id_lieu),
    CONSTRAINT chk_lieu_statut   CHECK (statut IN ('OUVERT','FERME','BLOQUE')),
    CONSTRAINT chk_lieu_capacite CHECK (capacite_max > 0)
);

-- ============================================================================
-- 4. BADGE
-- ============================================================================
CREATE TABLE BADGE (
    num_badge       VARCHAR2(50) NOT NULL,
    date_emission   DATE         DEFAULT SYSDATE NOT NULL,
    date_expiration DATE         NOT NULL,
    statut          VARCHAR2(20) DEFAULT 'ACTIF' NOT NULL,
    id_personne     NUMBER       NOT NULL,
    CONSTRAINT pk_badge          PRIMARY KEY (num_badge),
    CONSTRAINT fk_badge_personne FOREIGN KEY (id_personne) REFERENCES PERSONNE(id_personne),
    CONSTRAINT chk_badge_statut  CHECK (statut IN ('ACTIF','SUSPENDU','EXPIRE','REVOQUE','BLOQUE')),
    CONSTRAINT chk_badge_dates   CHECK (date_expiration > date_emission)
);

-- ============================================================================
-- 5. ACCES
-- ============================================================================
CREATE TABLE ACCES (
    id_acces      NUMBER GENERATED BY DEFAULT AS IDENTITY,
    libelle_acces VARCHAR2(100) NOT NULL,
    type_acces    VARCHAR2(30)  NOT NULL,
    description   VARCHAR2(255),
    statut        VARCHAR2(20)  DEFAULT 'ACTIF' NOT NULL,
    id_lieu       NUMBER        NOT NULL,
    CONSTRAINT pk_acces      PRIMARY KEY (id_acces),
    CONSTRAINT fk_acces_lieu FOREIGN KEY (id_lieu) REFERENCES LIEU(id_lieu),
    CONSTRAINT chk_type_acces   CHECK (type_acces IN ('PORTE','BARRIERE','PORTILLON','PARKING','ASCENSEUR')),
    CONSTRAINT chk_acces_statut CHECK (statut IN ('ACTIF','INACTIF'))
);

-- ============================================================================
-- 6. DROIT_ACCES
-- ============================================================================
CREATE TABLE DROIT_ACCES (
    id_droit     NUMBER GENERATED BY DEFAULT AS IDENTITY,
    id_profil    NUMBER        NOT NULL,
    id_lieu      NUMBER        NOT NULL,
    jour_semaine VARCHAR2(15)  NOT NULL,
    heure_debut  NUMBER(4)     NOT NULL,
    heure_fin    NUMBER(4)     NOT NULL,
    CONSTRAINT pk_droit_acces      PRIMARY KEY (id_droit),
    CONSTRAINT fk_droit_profil     FOREIGN KEY (id_profil) REFERENCES PROFIL(id_profil),
    CONSTRAINT fk_droit_lieu       FOREIGN KEY (id_lieu) REFERENCES LIEU(id_lieu),
    CONSTRAINT chk_jour_semaine    CHECK (jour_semaine IN ('LUNDI','MARDI','MERCREDI','JEUDI','VENDREDI','SAMEDI','DIMANCHE','TOUS')),
    CONSTRAINT chk_heure_debut     CHECK (heure_debut BETWEEN 0 AND 2359),
    CONSTRAINT chk_heure_fin       CHECK (heure_fin BETWEEN 0 AND 2359),
    CONSTRAINT chk_heure_coherence CHECK (heure_fin > heure_debut),
    CONSTRAINT uq_droit_unique     UNIQUE (id_profil, id_lieu, jour_semaine)
);

-- ============================================================================
-- 7. HISTORIQUE_PROFIL
-- ============================================================================
CREATE TABLE HISTORIQUE_PROFIL (
    id_historique_profil NUMBER GENERATED BY DEFAULT AS IDENTITY,
    id_personne          NUMBER        NOT NULL,
    id_ancien_profil     NUMBER,
    id_nouveau_profil    NUMBER        NOT NULL,
    date_changement      TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,
    motif                VARCHAR2(255),
    effectue_par         VARCHAR2(100) NOT NULL,
    CONSTRAINT pk_hist_profil        PRIMARY KEY (id_historique_profil),
    CONSTRAINT fk_hist_prof_personne FOREIGN KEY (id_personne) REFERENCES PERSONNE(id_personne),
    CONSTRAINT fk_hist_prof_ancien   FOREIGN KEY (id_ancien_profil) REFERENCES PROFIL(id_profil),
    CONSTRAINT fk_hist_prof_nouveau  FOREIGN KEY (id_nouveau_profil) REFERENCES PROFIL(id_profil)
);

-- ============================================================================
-- 8. HISTORIQUE_EVENEMENT
-- ============================================================================
CREATE TABLE HISTORIQUE_EVENEMENT (
    id_evenement              NUMBER GENERATED BY DEFAULT AS IDENTITY,
    type_evenement            VARCHAR2(30)  NOT NULL,
    date_evenement            TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,
    statut_resultat           VARCHAR2(20)  NOT NULL,
    description_justification VARCHAR2(255) NOT NULL,
    num_badge                 VARCHAR2(50),
    id_acces                  NUMBER,
    id_lieu                   NUMBER,
    id_personne               NUMBER,
    effectue_par              VARCHAR2(100),
    CONSTRAINT pk_hist_evenement PRIMARY KEY (id_evenement),
    CONSTRAINT fk_hist_badge     FOREIGN KEY (num_badge)  REFERENCES BADGE(num_badge),
    CONSTRAINT fk_hist_acces     FOREIGN KEY (id_acces)   REFERENCES ACCES(id_acces),
    CONSTRAINT fk_hist_lieu      FOREIGN KEY (id_lieu)    REFERENCES LIEU(id_lieu),
    CONSTRAINT fk_hist_personne  FOREIGN KEY (id_personne) REFERENCES PERSONNE(id_personne),
    CONSTRAINT chk_type_evt      CHECK (type_evenement IN (
        'ACCES_PHYSIQUE','CREATION_BADGE','MODIF_BADGE',
        'BLOCAGE_BADGE','DEBLOCAGE_BADGE',
        'MODIF_LIEU','BLOCAGE_LIEU','DEBLOCAGE_LIEU','MODIF_PROFIL'
    )),
    CONSTRAINT chk_statut_resultat CHECK (statut_resultat IN ('ACCORDE','REFUSE','SUCCES','ECHEC'))
);

